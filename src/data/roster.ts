// School roster used to validate participants at join time.
//
// Student id format is GCNN (4 digits): grade(1) + class(1) + number(2),
// e.g. 1101 = 1학년 1반 1번. To add more classes, append [studentId, name]
// pairs to REAL_ROSTER below.

export type RosterEntry = {
  studentId: string;
  name: string;
  grade: number;
  classNo: number;
  number: number;
};

function entry(studentId: string, name: string): RosterEntry {
  return {
    studentId,
    name,
    grade: Number(studentId[0]),
    classNo: Number(studentId[1]),
    number: Number(studentId.slice(2)),
  };
}

